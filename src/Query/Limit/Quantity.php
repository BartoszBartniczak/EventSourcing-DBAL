namespace BartoszBartniczak\EventSourcing\DBAL\Query\Limit;


class Quantity implements Limit
{

    /**
     * @var int
     */
    protected $value;

    /**
     * Quantity constructor.
     * @param int $value
     */
    public function __construct(int $value)
    {
        $this->value = $value;
    }

    /**
     * @return int
     */
    public function getValue(): int
    {
        return $this->value;
    }

}