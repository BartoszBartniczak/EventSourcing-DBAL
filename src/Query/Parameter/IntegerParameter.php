namespace BartoszBartniczak\EventSourcing\DBAL\Query\Parameter;


class IntegerParameter extends Parameter
{
    /**
     * @var int
     */
    protected $value;

    /**
     * Integer constructor.
     */
    public function __construct(int $value)
    {
        $this->value = $value;
    }

    /**
     * @return int
     */
    public function getValue(): int
    {
        return $this->value;
    }


}