namespace BartoszBartniczak\EventSourcing\DBAL\Query\Parameter;


class StringParameter extends Parameter
{

    /**
     * StringParameter constructor.
     */
    public function __construct(string $value)
    {
        $this->value = $value;
    }

    /**
     * @return string
     */
    public function getValue(): string
    {
        return $this->value;
    }


}