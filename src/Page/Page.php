namespace BartoszBartniczak\EventSourcing\DBAL\Page;


class Page
{

    private $value;

    /**
     * Page constructor.
     * @param $value
     */
    public function __construct($value)
    {
        $this->value = $value;
    }

    /**
     * @return mixed
     */
    public function getValue()
    {
        return $this->value;
    }


}